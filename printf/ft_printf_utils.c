#include "ft_printf.h"

int	ft_putchar(int c)
{
	write(1, &c, 1);
	return (1);
}

int	ft_putstr(char *s)
{
	int len;

	if (!s)
		return 0;
	len = -1;
	while (s[++len])
		ft_putchar(s[len]);
	return len;
}