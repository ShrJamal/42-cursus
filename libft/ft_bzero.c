#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	while (n--)
		*((char *)(s + n)) = 0;
}

int	main(void)
{
	char	str[] = "abcsd";

	ft_bzero(str, 2);
	printf("res: $%s$", str);
}
